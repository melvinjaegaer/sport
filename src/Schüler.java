public class Schüler {



    public class schüler {


        String vorname = "";
        String nachname = "";


        schüler(String pvorname, String pnachname) {

            vorname = pvorname;
            nachname = pnachname;


        }



        public String getVorname() {
            return vorname;
        }

        public String getNachname() {
            return nachname;
        }



        public void setVorname(String pVorname) {
            vorname = pVorname;
        }

        public void setNachname(String pNachname) {
            nachname = pNachname;
        }






    }}
